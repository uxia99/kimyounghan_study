package basic.loop;

public class While1_1 {
    public static void main(String[] args) {
        int count = 0;

        count++;
        System.out.println("현재 숫자는 : " + count); // 1
        count++;
        System.out.println("현재 숫자는 : " + count); // 2
        count++;
        System.out.println("현재 숫자는 : " + count); // 3
    }
}
