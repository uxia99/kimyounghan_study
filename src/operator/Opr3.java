package operator;

public class Opr3 {
    public static void main(String[] args) {
        int sum1 = 1 + 2 * 3;
        int sum2 = (1 + 2) * 3;
        // soutv 로 하면 자동으로 코드가 써지되, 변수 선택 가능
        System.out.println("sum1 = " + sum1); // 7
        System.out.println("sum2 = " + sum2); // 9
    }
}
