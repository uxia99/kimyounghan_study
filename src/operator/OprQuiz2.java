package operator;

public class OprQuiz2 {
    public static void main(String[] args) {
        double var1 = 1.5;
        double var2 = 2.5;
        double var3 = 3.5;

        double sum = var1 + var2 + var3;
        double average = sum / 3;

        System.out.println("Sum : " + sum); // Sum : 7.5
        System.out.println("Average : " + average); // Average : 2.5
    }
}
